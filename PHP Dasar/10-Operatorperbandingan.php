<?php 

$a = 10;
$b = "10";

// sama dengan
var_dump($a == $b);
// tidak sama dengan
var_dump($a != $b);
// identik
var_dump($a === $b);
// tidak identik
($a !== $b);
// kurang dari
var_dump($a < $b);
// kurang dari atau sama dengan
var_dump($a <= $b);
// lebih dari
var_dump($a > $b);
// lebih dari atau sama dengan
var_dump($a >= $b);
